function duration(data) {
  let durations = [];

  data.forEach(workout => {
    workout.exercises.forEach(exercise => {
      durations.push(exercise.duration);
    });
  });

  return durations;
}


//Added this to reflect duration sums on single workouts
function durationbyworkout(data) {
  let dbyworkout = [];

  data.forEach(workout => {
    intvalue=[]
    workout.exercises.forEach(exercise => {
      intvalue.push(exercise.duration);
    })
    total=0
    for(var i in intvalue) { total += intvalue[i]; }
    dbyworkout.push(total)
    ;
  });

  return dbyworkout;
}

//I added this to create date labeling (to replace day-of-week labeling)
function date(data) {
  let dates = [];

  data.forEach(workout => {
      dates.push(workout.day);
    });
  
  return dates;
}

function calculateTotalWeight(data) {
  let total = [];

  data.forEach(workout => {
    workout.exercises.forEach(exercise => {
      total.push(exercise.weight);
    });
  });

  return total;
}

function workoutNames(data) {
  let workouts = [];

  data.forEach(workout => {
    workout.exercises.forEach(exercise => {
      workouts.push(exercise.name);
    });
  });
  
  return workouts;
}
